/******************DMA发送函数*******************/
void UsartSendData_DMA(uint8_t channel,uint8_t *pdata,uint16_t Length)
{
	switch (channel)
	{
		case 1:
		{
			if(USART1_STA.TX_FLAG==0) //忙则错过
			{
					USART1_STA.TX_FLAG =1;
					HAL_UART_Transmit_DMA(&huart1,pdata,Length);	
			}						
		}break;
		case 2:
		{
			if(USART2_STA.TX_FLAG==0) 
			{
					USART2_STA.TX_FLAG =1;
					HAL_UART_Transmit_DMA(&huart2,pdata,Length);	
			}						
		}break;
		case 3:
		{
			if(USART3_STA.TX_FLAG==0) 
			{
					USART3_STA.TX_FLAG =1;
				  DIR3=1;
					HAL_UART_Transmit_DMA(&huart3,pdata,Length);	
			}						
		}break;
		case 4:
		{
			if(UART4_STA.TX_FLAG==0) 
			{
					UART4_STA.TX_FLAG =1;
					HAL_UART_Transmit_DMA(&huart4,pdata,Length);	
			}						
		}break;
		case 5:
		{
			if(UART5_STA.TX_FLAG==0) 
			{
					UART5_STA.TX_FLAG =1;
					HAL_UART_Transmit_DMA(&huart5,pdata,Length);	
			}						
		}break;
		
		default:
			break;
	}
}
